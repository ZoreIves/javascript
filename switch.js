
function Nilai(){
    var nilaiMatematika = 90;

    switch(nilaiMatematika){
        case 100:
            console.log("Sempurna");
            break;
    
        case 90:
            console.log("Sangat Bagus");
            break;
    
        case 80:
            console.log("Bagus");
            break;
    
        case 70:
            console.log("Cukup Bagus");
            break;
    
        default:
            console.log("Kurang");
    }
}

console.log(Nilai());

